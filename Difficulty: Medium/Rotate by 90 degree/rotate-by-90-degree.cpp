/* matrix : given input matrix, you are require
 to change it in place without using extra space */
void rotate(vector<vector<int> >& mat) {
    // Your code goes here
    int r = mat.size();
    int c = mat[0].size();
    vector<vector<int>> ans(c,vector<int>(r));
    for(int i=0;i<r;i+=1) {
        for(int j=0;j<c;j+=1) {
            ans[j][r-i-1]=mat[i][j];
        }
    }
    mat=ans;
}


