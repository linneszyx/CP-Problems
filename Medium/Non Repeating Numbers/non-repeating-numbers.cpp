class Solution
{
public:
    vector<int> singleNumber(vector<int> nums) 
    {
        // Code here.
    sort(nums.begin(),nums.end());
    vector<int> v;
    map<int,int> ans;
    for(int i=0;i<nums.size();i++) ans[nums[i]]++;
    for(auto x : ans){
        if(x.second==1) v.push_back(x.first);
    }
    return v;
    }
};

