class Solution
{
    public:
    //Function to find position of first set bit in the given number.
    unsigned int getFirstSetBit(int n)
    {
        bool f = true;
        int c = 0;
        if(n==0) return 0;
        while(f){
            if((n&1)==1) {
                f = false;
                c++;
                break;
            }
            c++;
            n/=2;
        }
        return c;
        // Your code here
    }
};

