class Solution {
  public:
    int countPairs(vector<int> &arr, int target) {
        // Code here
        unordered_map<int,int> mp;
        int c=0;
        int n = arr.size();
        for(int i=0;i<n;i+=1) {
            if(mp.find(target-arr[i])!=mp.end()) c+=mp[target-arr[i]];
            mp[arr[i]]++;
        }
        return c;
    }
};

