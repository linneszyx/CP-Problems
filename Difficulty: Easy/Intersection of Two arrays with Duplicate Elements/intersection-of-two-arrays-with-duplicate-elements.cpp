class Solution {
  public:
    vector<int> intersectionWithDuplicates(vector<int>& a, vector<int>& b) {
        set<int> s;
        for(int i=0;i<a.size();i+=1) {
            int c=0;
            for(int j=0;j<b.size();j+=1) {
                if(a[i]==b[j]) {
                    c=1;
                    break;
                }
            }
            if(c==1) s.insert(a[i]);
        }
        vector<int> r;
        for(auto &x:s) r.push_back(x);
        return r;
    }
};

